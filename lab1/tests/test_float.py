import pytest

from src.binary_int import Binary_float

class TestClassFloatBinary:
    @pytest.mark.parametrize(
        "value, natation, expected", 
        [
            ('0', 10, '00000000011111111111111111111111'), 
            ('+1', 10, '0 01111111 00000000000000000000000'),             
            ('2', 10, '0 10000000 00000000000000000000000'),             
            ('3', 10, '0 10000000 10000000000000000000000'),             
            ('4', 10, '0 10000001 00000000000000000000000'),             
            ('5', 10, '0 10000001 01000000000000000000000'),             
            ('+6', 10, '0 10000001 10000000000000000000000'),             
            ('+7', 10, '0 10000000 00000000000000000000000'),             
            ('8', 10, '0 10000010 00000000000000000000000'),             
            ('9', 10, '0 10000010 00100000000000000000000'),             
            ('-1', 10, '1 01111111 00000000000000000000000'),             
            ('-2', 10, '1 10000000 00000000000000000000000'),             
            ('-3', 10, '1 10000000 10000000000000000000000'),             
            ('-4', 10, '1 10000001 00000000000000000000000'),             
            ('-5', 10, '1 10000001 01000000000000000000000'),
            ('00000000100000000000000001000110', 2,
             '00000000100000000000000001000110'),             
            ('00000000000110000000000000100110', 2, 
             '00000000000110000000000000100110'),             
            ('10000000000000000000000001011111', 2, 
             '10000000000000000000000001011111')])
    def test_good_init_str(self, value, natation, expected):
        binary = Binary_float(value, natation)
        assert str(binary) == expected
